    private void register_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_register_buttonActionPerformed
        // TODO add your handling code here:
        if("EMAIL".equals(email_field.getText())){
            JOptionPane.showMessageDialog(rootPane, "NO FIELD CAN BE EMPTY!");
        }
        else{
            try {
            conn = DbConnection.dbConnect();
            stmt = conn.createStatement();
            String sql = "SELECT * FROM admin WHERE email='" + email_field.getText() + "'";
            ResultSet rs = stmt.executeQuery(sql);

            boolean userExists = rs.next();
            if (userExists) {
                JOptionPane.showMessageDialog(rootPane, "User already registered");
            } else {
                if (confirm_password_field.getText().equals(password_field.getText())) {
                    String insertSql = "INSERT INTO admin (username, password, email) VALUES (?, ?, ?)";
                    PreparedStatement insertStmt = conn.prepareStatement(insertSql);
                    insertStmt.setString(1, username_field.getText());
                    insertStmt.setString(2, password_field.getText());
                    insertStmt.setString(3, email_field.getText());

                    insertStmt.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "User registered successfully");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "The password does not match the confirm password");
                }
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

        }
    }//GEN-LAST:event_register_buttonActionPerformed

